#include "util.h"

// implementation for util.h

// returns x + y
int add(int x, int y)
{
    return x + y;
}

// returns x - y
int subtract(int x, int y)
{
    return y - x;
}

// returns x * y
int multiply(int x, int y)
{
    return x * y;
}

// returns y / x
float divide(int x, int y)
{
    return (float)y / (float)x;
}